        protected function DoBeforeCreate()
        {
            $selectQuery = 'SELECT 
              productos.idproducto, 
              productos.nombre, 
              caracteristicas.marca,  
              to_char(productos.precio_venta,\'LFM9,999,999\') as precio, 
              productos.stock, 
              unidades.descripcion, 
              descuentos.descpcion_descuento
            FROM 
              public.productos, 
              public.descuentos, 
              public.unidades, 
                public.caracteristicas
            WHERE 
              descuentos.iddescuentos = productos.iddescuentos AND
              unidades.idunidad = productos.idunidad AND
                caracteristicas.idcaracter = productos.idcaracter AND
              productos.estado = 1';
            $insertQuery = array();
            $updateQuery = array();
            $deleteQuery = array();
            $this->dataset = new QueryDataset(
              PgConnectionFactory::getInstance(), 
              GetConnectionOptions(),
              $selectQuery, $insertQuery, $updateQuery, $deleteQuery, 'Consultar_Precio_producto');
            $field = new IntegerField('idproducto');
            $this->dataset->AddField($field, true);
            $field = new StringField('nombre');
            $this->dataset->AddField($field, false);
            $field = new StringField('marca');
            $this->dataset->AddField($field, false);
            $field = new StringField('precio');
            $this->dataset->AddField($field, false);
            $field = new IntegerField('stock');
            $this->dataset->AddField($field, false);
            $field = new StringField('descripcion');
            $this->dataset->AddField($field, false);
            $field = new StringField('descpcion_descuento');
            $this->dataset->AddField($field, false);
        }
    
