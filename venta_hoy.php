        protected function DoBeforeCreate()
        {
            $selectQuery = 'SELECT   facturar_ventas.n_factura,
                      sum(facturar_ventas.valor_total_pagar) as totalventa, 
                    sum(facturar_ventas.utilidad_en_venta) as utlida       
              FROM   facturar_ventas
              WHERE  to_char(facturar_ventas.fecha_hora,\'YYYY-MM-DD\')=to_char(now(),\'YYYY-MM-DD\')
              
              GROUP BY facturar_ventas.n_factura';
            $insertQuery = array();
            $updateQuery = array();
            $deleteQuery = array();
            $this->dataset = new QueryDataset(
              PgConnectionFactory::getInstance(), 
              GetConnectionOptions(),
              $selectQuery, $insertQuery, $updateQuery, $deleteQuery, 'venta_hoy');
            $field = new IntegerField('n_factura');
            $this->dataset->AddField($field, true);
            $field = new IntegerField('totalventa');
            $this->dataset->AddField($field, false);
            $field = new IntegerField('utlida');
            $this->dataset->AddField($field, false);
            $this->dataset->AddCustomCondition(EnvVariablesUtils::EvaluateVariableTemplate($this->GetColumnVariableContainer(), 'n_factura!=n_factura'));
        }
    
