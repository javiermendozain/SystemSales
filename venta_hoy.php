        protected function setupCharts()
        {
            $sql = 'SELECT   facturar_ventas.n_factura,
                        to_char(now(),\'YYYY-Mon-DD\')as fecha,
                      sum(facturar_ventas.valor_total_pagar) as totalventa, 
                    sum(facturar_ventas.utilidad_en_venta) as utlida       
              FROM   facturar_ventas
              WHERE  to_char(facturar_ventas.fecha_hora,\'YYYY-MM-DD\')=to_char(now(),\'YYYY-MM-DD\')
             GROUP BY facturar_ventas.n_factura';
            
            $chart = new Chart('ventahoy', Chart::TYPE_COLUMN, $this->dataset, $sql);
            $chart->setTitle($this->RenderText(''));
            $chart->setDomainColumn('fecha', $this->RenderText('Fecha'), 'string');
            $chart->addDataColumn('totalventa', $this->RenderText('Total Venta'), 'float', '$ ##,###,###');
            $chart->addDataColumn('utlida', $this->RenderText('Utilidad'), 'float', '$ ##,###,###');
            $this->addChart($chart, 0, ChartPosition::BEFORE_GRID, 12);
        }
    
